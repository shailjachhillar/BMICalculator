import 'dart:math';

class CalculatorBrain {
  CalculatorBrain({this.height, this.weight});
  final int height;
  final int weight;
  double _bmi;
  String CalculateBMI() {
    _bmi = weight / pow(height / 100, 2);
    return _bmi.toStringAsFixed(1);
  }

  String getResult() {
    if (_bmi >= 25) {
      return 'OVERWEIGHT';
    } else if (_bmi > 18.5) {
      return 'NORMAL';
    } else {
      return 'UNDERWEIGHT';
    }
  }

  String getInterpretation() {
    if (_bmi >= 25) {
      return 'START EXERCISING MORE YOU FATSO !!!';
    } else if (_bmi > 18.5) {
      return 'YOU ARE FIT ... I LIKE FIT PEOPLE !';
    } else {
      return 'EAT MORE ELSE YOU' 'LL DIE !!';
    }
  }
}
